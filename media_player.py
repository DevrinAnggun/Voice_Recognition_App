class MediaPlayer:
    def playSong(self):
        print("Memutar lagu favorit...")

    def pause(self):
        print("Video dijeda.")

    def nextTrack(self):
        print("Lagu berikutnya diputar.")

    def stop(self):
        print("Musik dihentikan.")

    def previousTrack(self):
        print("Kembali ke lagu sebelumnya.")

    def volumeUp(self):
        print("Volume dinaikkan.")

    def volumeDown(self):
        print("Volume diturunkan.")
